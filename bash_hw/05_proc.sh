#!/bin/bash

# Запустим три команды sleep в фоновом режиме
sleep 5 &
sleep 10 &
sleep 15 &

# Выведем список фоновых задач
echo "Список фоновых задач:"
jobs

# С помощью fg переведем одну из задач на передний план
echo "Перемещение задачи sleep 10 на передний план (fg):"
fg %2

# Вернем обратно на задний
echo "Возвращение задачи sleep 10 на задний план (bg):"
bg %2

# Выведем список перемещения
echo "Список фоновых задач после перемещения:"
jobs

# Подождем завершения всех фоновых задач
echo "Подождем завершения всех фоновых задач"
wait

echo "Все фоновые задачи завершены."
